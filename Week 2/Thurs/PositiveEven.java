class PositiveEven
{
	public static void main(String args[])
	{
		int num = 11;
		 if(num<=1)
		 {
			 System.out.println("Number is not positive !! ");
		 }
		 else if(num>1 && num%2==0)
		 {
			 System.out.println("Number is positive !! ");
		 }
		 else
		 {
			 System.out.println("Number is not positive !! ");
		 }
	}
}